let translate = function (value) {
  if (typeof localStorage !== 'undefined') {
    let language = localStorage.getItem('netaLang')

    if (typeof lang[value] !== 'undefined') {
        if (language === JSON.stringify('en')) {
          return value
        } else if (language === JSON.stringify('vi')) {
          return lang[value]
          }
      return lang[value]
    } else {
      return value
    }  
  }
}


export default translate
